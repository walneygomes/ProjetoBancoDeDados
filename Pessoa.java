package br.com.bancodados2;

public class Pessoa {
	private String _nome;
	private String _cpf;
	private int _idade;

	public String get_nome() {
		return _nome;
	}

	public void set_nome(String _nome) {
		this._nome = _nome;
	}

	public String get_cpf() {
		return _cpf;
	}

	public void set_cpf(String _cpf) {
		this._cpf = _cpf;
	}

	public int get_idade() {
		return _idade;
	}

	public void set_idade(int _idade) {
		this._idade = _idade;
	}

}
